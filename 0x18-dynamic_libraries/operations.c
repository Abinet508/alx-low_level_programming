/**
  * add - calculates sum of two numbers.
  * @a: number
  * @b: number
  *
  *Return: sum.
  */
int add(int a, int b)
{
	return (a + b);
}

/**
  * sub - calculates difference
  * @a: number;
  * @b: number;
  *
  * Return: difference.
  */
int sub(int a, int b)
{
	return (a - b);
}

/**
  * mul - calculates product of two numbers
  * @a: number.
  * @b: number.
  *
  * Return: product.
  */
int mul(int a, int b)
{
	return ((a) * (b));
}

/**
  * div - calculates the quotient of two numbers.
  * @a: number
  * @b: number.
  *
  * Return: quotient.
  */
int div(int a, int b)
{
	return ((a) / (b));
}

/**
  * mod - calculates teh modulus of two numbers
  * @a: number.
  * @b: number.
  *
  * Return: modulus.
  */
int mod(int a, int b)
{
	return ((a) % (b));
}
